#include <libft.h>

void	ft_echo(char **av)
{
	char	*verrif;

	if ((verrif = *(av + 1)))
		while (*(++av))
		{
			if (verrif != *av)
				write(1, " ", 1);
			ft_putstr(*av);
		}
	write(1, "\n", 1);
}
